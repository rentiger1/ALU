                    <h3 class="site-heading text-center text-white d-none d-lg-block">
                        Alliance LARP Consent Policy
                    </h3>

                    <span class="mb-0">
                        <p>
                            Every attendee has the right to feel physically and emotionally safe at events. 
                            All persons are responsible for making this environment safe. &nbsp;If for any 
                            reason you do not feel safe, please let someone, preferably staff know. If we know 
                            then we can help. You are welcome to reach out to us after events if you are unable 
                            to address it at the time. Players will not be reprehended IG (In Game) for taking time OOG (Out of Game).
                        </p>
                        <p>We take people&rsquo;s safety and consent seriously. At NO time should any player ever
                            be asked to subsume their OOG boundaries to serve their character&rsquo;s IG motivation 
                            or verisimilitude. It is of our utmost concern that everyone feels respected. We feel 
                            that with this respect of boundaries people can have maximum entertainment. In the long 
                            term, respect can form trust and with trust we can go farther together, if wanted.
                        </p>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; As defined by us, consent 
                            means that people know what they are getting into and they are excited to participate the 
                            entire time it is occurring. Consent can be withdrawn at any time, for any or no reason.
                        </p>
                        <p>
                            <strong>Consent</strong>-Fully informed, enthusiastic, continuous, agreement to an activity.
                        </p>
                        <p>
                            We strive for an environment where; people&rsquo;s autonomy will not be challenged and 
                            all people feel the safety of having the option to opt in or out of a scenario.
                        </p>
                        <p>
                            <u>What is not allowed OOG:</u>
                            <br><u>&nbsp;</u>
                        </p>
                        <p><u>Unwanted touching</u> - Touching someone's body or possessions without prior permission. 
                            Examples: Playing with a stranger's hair or grabbing someone's prop.
                        </p>

                        <p>
                            <u>Coercion</u> -the practice of persuading someone to do something by using force or threats
                            - Example: "If you don't _____, then I will _____."
                        </p>

                        <p>
                            <u>Disregarding Boundaries</u> - Doing something after being explicitly told that it is not 
                            acceptable - Example: Kissing someone after they say they don't like kissing.
                        </p>

                        <p>
                            <u>Gas lighting </u>- to manipulate (someone) by psychological means into questioning their
                            own sanity - Example: "That's not what happened at all. I'm not sure why you've created this narrative in your head."
                        </p>

                        <p>
                            <u>Psychological Manipulation</u> - a type of social influence that aims to change 
                            the behavior or perception of others through abusive, deceptive, or underhanded tactics.
                        </p>

                        <p>
                            <u>Emotional manipulation</u> - Similar to psychological manipulation, this tactic 
                            provokes guilt, love, and other emotional attachments to change another's behavior.
                        </p>

                        <p>
                            If people are seen or reported as violating consent actions will be taken to correct
                            the behavior or punishment up to being banned.
                        </p>

                        <p>
                            If folks would like to negotiate and consent to any of these actions strictly IG, that
                            is fine. If assistance negotiating is needed please ask a Consent Marshall, we are 
                            more than willing to help.
                        </p>
                        <p>Consent is on a case-by-case basis, you must obtain fresh consent every time. Long standing
                            negotiations are allowed and checking in are encouraged. &nbsp;If at any point you or the
                            other party wish to terminate the agreement, you and the other party can do so. 
                            Explanations are a courtesy and not mandatory.
                        </p>

                        <p>
                            <u>Negotiation:</u>
                        </p>
                        <p>
                            &nbsp;&nbsp;To improve the play of all persons involved, we highly encourage negation both
                            before and during game. If you wish to do physical, romantic or any emotionally intense 
                            role play, briefly go out of game by placing a hand on your head and asking for consent. 
                            Please include decent detail so that the other party may be fully informed. Please remember 
                            that OOG boundaries are not subject to either IG or OOG pressure or consequences.
                        </p>

                        <p>
                            <strong>Physical Role Play Example:</strong>
                        </p>

                        <p>
                            &nbsp;If Player 1 wants to engage in physical roleplay with Player 2 outside of combat 
                            (physical contact except with a weapon remains expressly forbidden in combat), they briefly 
                            go out of game by placing a hand on their head and expressly requesting the specific physical 
                            interaction they are seeking, i.e.: &ldquo;Physical role play negotiation: May I kiss you?&rdquo; 
                            Player two now has numerous options, because this is stated to be a negotiation.
                        </p>
                        <p>
                            Options may include:
                        </p>
                        <p>
                            &ldquo;No&rdquo;
                        </p>
                        <p>
                            &ldquo;No, I can do a hug though.&rdquo;
                        </p>
                        <p>
                            &ldquo;I am not comfortable with a kiss, but my character would be, so I am comfortable 
                            simulating a kiss, or simply saying that they did without completing the action.&rdquo;
                        </p>
                        <p>
                            &ldquo;Yes, I am comfortable with a brief, closed-mouth kiss&rdquo;
                        </p>
                        <p>
                            &ldquo;Yes, I am comfortable with a lingering, open-mouth kiss&rdquo;
                        </p>

                        <p>
                            At any point physical contact with the intent to cause bodily harm is not allowed.
                        </p>

                        <p>
                            <strong>Romantic Role Play Example:</strong>
                        </p>

                        <p>
                            If player 1 wishes to engage in role play of physical or romantic attraction to player 2, 
                            they briefly go out of game by placing a hand on their head and stating &ldquo;Roleplay 
                            negotiation&rdquo;, followed by the specific form of attraction that they wish to roleplay 
                            going forward. Ie:
                        </p>
                        <p>
                            &ldquo;Roleplay negotiation; do you consent to my character having a romantic crush on yours?&rdquo;
                        </p>

                        <p>
                            Player two now has a number of options, including:
                        </p>
                        <p>
                            &ldquo;No, thank you.&rdquo;
                        </p>
                        <p>
                            &ldquo;Yes, and I may reciprocate the crush, if that is acceptable.&rdquo;
                        </p>
                        <p>
                            &ldquo;Yes, but if you choose to do so the crush will be entirely unreciprocated&rdquo;
                        </p>

                        <p>
                            Note that romantic roleplay consent does NOT grant physical roleplay consent unless explicitly stated.
                            A romantic role play negotiation that does include physical role play negotiation might read as follows:
                        </p>

                        <p>
                            Player 1: &ldquo;Roleplay negotiation, do you consent to my character feeling romantic attraction to yours?&rdquo;
                        </p>
                        <p>
                            Player 2: &ldquo;Yes, and my character will likely reciprocate, but only briefly before losing interest&rdquo;
                        </p>
                        <p>
                            Player 1: &ldquo;Are you comfortable with physical roleplay expressed as hand holding, cuddling, and/or kissing?&rdquo;
                        </p>
                        <p>
                            Player 2: &ldquo;Kissing is unacceptable for me, but hand holding and cuddling are fine.&rdquo;
                        </p>
                        <p>
                            Player 1: &ldquo;Okay!&rdquo;
                        </p>

                        <p>
                            &nbsp;Please remember that while the roleplay being negotiated is in game, the negotiation is entirely 
                            out of game. This means that if your request consent to express romantic interest in a character, and 
                            the player declines during negotiation, your character has not been rejected in game. Rather, your character
                            has never felt an attraction to the other person&rsquo;s. Thus, playing &ldquo;the scorned lover&rdquo; in 
                            this situation is inappropriate, and may be viewed as in-game retaliation for an out-of-game rebuff, and harassment.
                        </p>

                        <p>
                            If any player feels at any time that their consent is not being respected (if a player engages in unwelcome physical
                            roleplay without requesting consent, for instance, or after consent has been revoked) or is experiencing retaliation, 
                            this is an addressable concern, and should be brought to staff attention.
                        </p>

